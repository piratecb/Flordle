  /// Retorna a palavra para uma data específica
  /// Usa ano + dia do ano para garantir palavras diferentes a cada ano
  static String getWordForDate(DateTime date) {
    // Calcula o dia do ano (0-364)
    final dayOfYear = date.difference(DateTime(date.year, 1, 1)).inDays;
    // Combina ano e dia para gerar um índice único
    // Assim, 2 Jan 2026 terá palavra diferente de 2 Jan 2027
    final seed = (date.year * 366) + dayOfYear;
    final index = seed % words.length;
    return words[index];
  }

  /// Retorna a palavra de hoje
  static String getTodayWord() {
    return getWordForDate(DateTime.now());
  }

  /// Retorna o número total de palavras
  static int get totalWords => words.length;

  /// Verifica se uma palavra existe na lista (para validação de palpites)
  static bool isValidWord(String word) {
    return words.contains(word.toUpperCase());
  }

  /// Preview das próximas N palavras (para debug/admin)
  static List<Map<String, String>> previewNextDays(int days) {
    final now = DateTime.now();
    List<Map<String, String>> preview = [];

    for (int i = 0; i < days; i++) {
      final date = now.add(Duration(days: i));
      final dateStr = '${date.year}-${date.month.toString().padLeft(2, '0')}-${date.day.toString().padLeft(2, '0')}';
      preview.add({
        'date': dateStr,
        'word': getWordForDate(date),
      });
    }

    return preview;
  }
